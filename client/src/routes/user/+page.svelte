<script lang="ts">
    import { error, user } from "$lib";
    import { fetchAPI } from "$lib/_core";

    let formView = $state("default")

    let data = $props()
    if(data.form && data.form.error){
        error.set(data.form.error)
    }

    let form:any = $state()
    let isSubmit:boolean = $state(false)
    let username:string = $state("")

    async function fetchUser(data:any){
        data.preventDefault()
        isSubmit = true
        const dataFetch = await fetchAPI(
            form.getAttribute("data-path"), 
            "POST", 
            new FormData(form)
        )
        isSubmit = false
        if(dataFetch.error){
            return
        }
        user.set(dataFetch)
    }

</script>

{#if formView === "default"}
    <h1>Connect</h1>
    <button type="button" 
    class="action outline secondary" 
    onclick={() => formView = "register"}>
    <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 448 512"><path d="M64 32C28.7 32 0 60.7 0 96L0 416c0 35.3 28.7 64 64 64l320 0c35.3 0 64-28.7 64-64l0-320c0-35.3-28.7-64-64-64L64 32zM200 344l0-64-64 0c-13.3 0-24-10.7-24-24s10.7-24 24-24l64 0 0-64c0-13.3 10.7-24 24-24s24 10.7 24 24l0 64 64 0c13.3 0 24 10.7 24 24s-10.7 24-24 24l-64 0 0 64c0 13.3-10.7 24-24 24s-24-10.7-24-24z"/></svg> Register
    </button>
    <form id="setConnect" method="POST" action="?/login">
        <input type="text" placeholder="Username" name="username" bind:value={username} />
        <button type="submit" disabled={isSubmit === true || username === ""}>
            <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 512 512"><path d="M217.9 105.9L340.7 228.7c7.2 7.2 11.3 17.1 11.3 27.3s-4.1 20.1-11.3 27.3L217.9 406.1c-6.4 6.4-15 9.9-24 9.9c-18.7 0-33.9-15.2-33.9-33.9l0-62.1L32 320c-17.7 0-32-14.3-32-32l0-64c0-17.7 14.3-32 32-32l128 0 0-62.1c0-18.7 15.2-33.9 33.9-33.9c9 0 17.6 3.6 24 9.9zM352 416l64 0c17.7 0 32-14.3 32-32l0-256c0-17.7-14.3-32-32-32l-64 0c-17.7 0-32-14.3-32-32s14.3-32 32-32l64 0c53 0 96 43 96 96l0 256c0 53-43 96-96 96l-64 0c-17.7 0-32-14.3-32-32s14.3-32 32-32z"/></svg> Connect
        </button>
    </form>
{:else if formView === "register"}
    <h1>Register</h1>
    <button type="button" 
    class="action outline secondary" 
    onclick={() => formView = "default"}>
    <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 512 512"><path d="M217.9 105.9L340.7 228.7c7.2 7.2 11.3 17.1 11.3 27.3s-4.1 20.1-11.3 27.3L217.9 406.1c-6.4 6.4-15 9.9-24 9.9c-18.7 0-33.9-15.2-33.9-33.9l0-62.1L32 320c-17.7 0-32-14.3-32-32l0-64c0-17.7 14.3-32 32-32l128 0 0-62.1c0-18.7 15.2-33.9 33.9-33.9c9 0 17.6 3.6 24 9.9zM352 416l64 0c17.7 0 32-14.3 32-32l0-256c0-17.7-14.3-32-32-32l-64 0c-17.7 0-32-14.3-32-32s14.3-32 32-32l64 0c53 0 96 43 96 96l0 256c0 53-43 96-96 96l-64 0c-17.7 0-32-14.3-32-32s14.3-32 32-32z"/></svg> Connect
    </button>
    <form id="setConnect" 
    bind:this={form}
    data-path="/user" 
    onsubmit={fetchUser}>
        <input type="text" placeholder="Username" name="username" bind:value={username} />
        <button type="submit" disabled={isSubmit === true || username === ""}>
            <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 448 512"><path d="M64 32C28.7 32 0 60.7 0 96L0 416c0 35.3 28.7 64 64 64l320 0c35.3 0 64-28.7 64-64l0-320c0-35.3-28.7-64-64-64L64 32zM200 344l0-64-64 0c-13.3 0-24-10.7-24-24s10.7-24 24-24l64 0 0-64c0-13.3 10.7-24 24-24s24 10.7 24 24l0 64 64 0c13.3 0 24 10.7 24 24s-10.7 24-24 24l-64 0 0 64c0 13.3-10.7 24-24 24s-24-10.7-24-24z"/></svg> Register
        </button>
    </form>
{/if}